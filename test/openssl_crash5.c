/* main                                                                 */
/* ================================================================== */

int main(void)
{
    printf("=== ML-KEM Codec Reproduction Test (chibicc compat) ===\n");
    printf("    (FIPS 203 / OpenSSL 15-test_ml_kem_codecs.t)\n\n");

    printf("-- Section 1: ByteEncode/ByteDecode (d=1..12) --\n");
    for (int d = 1; d <= 12; d++)
        test_byte_encode_decode(d);

    printf("\n-- Section 2: Compress/Decompress (d=1..11) --\n");
    for (int d = 1; d <= 11; d++)
        test_compress_decompress(d);

    printf("\n-- Section 3: Full codec (compress+encode / decode+decompress) --\n");
    int ds[] = { 1, 4, 5, 10, 11 };
    for (int i = 0; i < 5; i++)
        test_full_codec(ds[i]);

    printf("\n-- Section 4: Bit-manipulation patterns --\n");
    test_bit_patterns();

    printf("\n-- Section 5: NTT Montgomery reduction --\n");
    test_montgomery_reduce();

    printf("\n-- Section 6: Struct layout / field offsets --\n");
    test_struct_layout();

    printf("\n-- Section 7: Integer promotion edge cases --\n");
    test_integer_promotions();

    printf("\n========================================\n");
    printf("Results: %d/%d passed", g_tests - g_failed, g_tests);
    if (g_failed)
        printf(", %d FAILED  ← chibicc codegen bug(s) detected\n", g_failed);
    else
        printf(" — ALL PASS\n");
    printf("========================================\n");

    return (g_failed > 0) ? 1 : 0;
}
